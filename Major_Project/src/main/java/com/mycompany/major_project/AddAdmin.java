    public void AutoID(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/majorproject","root","199vedant");
            pre = con.prepareStatement("Select Max(AdminID) from admin");
            
            ResultSet rs = pre.executeQuery();
            rs.next();
            if(rs.getString("max(AdminID)") == null){
                adminid.setText("AD001");
            }
            else{
                int id = Integer.parseInt(rs.getString("max(AdminID)").substring(2));
                id++;
                adminid.setText("AD" + String.format("%03d", id));
            } 
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
