    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        String AdminId = adminid.getText();
        String FirstName = firstname.getText();
        String LastName = lastname.getText();
        String UserName = username.getText();
        String Password = password.getText();
        String Contact = contact.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/majorproject","root","199vedant");
            pre = con.prepareStatement("insert into admin (AdminID,firstname,lastname,contact,username,password) values(?,?,?,?,?,?)");
                        
            pre.setString(1, AdminId);
            pre.setString(2,FirstName);
            pre.setString(3, LastName);
            pre.setString(4, Contact);
            pre.setString(5,UserName);
            pre.setString(6,Password);
            
            pre.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Admin added Successfully");
            
            adminid.setText("");
            firstname.setText("");
            lastname.setText("");
            username.setText("");
            password.setText("");
            contact.setText("");
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addActionPerformed


